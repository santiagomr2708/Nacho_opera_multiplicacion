    public class multiplicacion
    {
       /// <summary>
       /// Metodo multiplicar
       /// </summary>
       /// <param name="i"> operando 1 </param>
       /// <param name="j"> operando 2 </param>
       /// <returns></returns>
       ///
       public static long multi(long i, long j)
        {
            return (i * j);
        }
    }
